import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: '*',
      allow: '/',
      disallow: ['/api/', '/dashboard/', '/login/', '/cadastro/'],
    },
    sitemap: 'https://www.engenhariabiomedica.com/sitemap.xml',
  };
}
